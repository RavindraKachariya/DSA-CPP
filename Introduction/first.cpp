    cout << "Enter b: ";
    cin >> b;

    c = a + b;
    cout << "The sum is: " << c << endl;

    cout << "----For Subtraction----" << endl;
    cout << "Enter a: ";
    cin >> a;

    cout << "Enter b: ";
    cin >> b;

    c = a - b;
    cout << "The sum is: " << c << endl;

    cout << "----For Multipication----" << endl;
    cout << "Enter a: ";
    cin >> a;

    cout << "Enter b: ";
    cin >> b;

    c = a * b;
    cout << "The sum is: " << c << endl;

    cout << "----For Division----" << endl;
    cout << "Enter a: ";
    cin >> a;

    cout << "Enter b: ";
    cin >> b;

    c = a / b;
    cout << "The sum is: " << c << endl;

